# 计算 QWK 的函数，针对不同的维度计算
def compute_prf1(eval_pred):
    #logits, labels = eval_pred

    predictions, labels = eval_pred

    #predictions = np.argmax(logits, axis=-1)

    # 将预测和标签转换为字符串
    pred_str = tokenizer.batch_decode(predictions, skip_special_tokens=True)
    labels = np.where(labels == -100, tokenizer.pad_token_id, labels)
    label_str = tokenizer.batch_decode(labels, skip_special_tokens=True)
    # Map predictions and labels to discrete values
    pred_ids = []
    label_ids = []
    for pred, label in zip(pred_str, label_str):
        if pred in CATEGORY_MAPPING and label in CATEGORY_MAPPING:
            pred_ids.append(CATEGORY_MAPPING[pred])
            label_ids.append(CATEGORY_MAPPING[label])

    # If no valid pairs, return 0 scores
    if not pred_ids:
        return {"precision": 0.0, "recall": 0.0, "f1": 0.0}

    # Calculate P, R, F1
    precision = precision_score(label_ids, pred_ids, labels=list(CATEGORY_MAPPING.values()), average='macro', zero_division=0)
    recall = recall_score(label_ids, pred_ids, labels=list(CATEGORY_MAPPING.values()), average='macro', zero_division=0)
    f1 = f1_score(label_ids, pred_ids, labels=list(CATEGORY_MAPPING.values()), average='macro', zero_division=0)

    return {"precision": precision, "recall": recall, "f1": f1}

# 定义 logits 的预处理函数
def preprocess_logits_for_metrics(logits, labels):
    """
    处理 LLaMA 的生成模型 logits，用于评估阶段。
    """
    # logits 的形状为 (batch_size, sequence_length, vocab_size)
    # 通常我们使用 argmax 转换 logits 为生成的 token 序列
    return logits.argmax(dim=-1)  # 返回 shape: (batch_size, sequence_length)


